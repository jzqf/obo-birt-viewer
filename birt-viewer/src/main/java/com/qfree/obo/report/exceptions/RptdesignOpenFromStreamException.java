package com.qfree.obo.report.exceptions;

public class RptdesignOpenFromStreamException extends ReportingException {

	public RptdesignOpenFromStreamException() {
	}

	public RptdesignOpenFromStreamException(String message) {
		super(message);
	}

	public RptdesignOpenFromStreamException(Throwable cause) {
		super(cause);
	}

	public RptdesignOpenFromStreamException(String message, Throwable cause) {
		super(message, cause);
	}

	public RptdesignOpenFromStreamException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
